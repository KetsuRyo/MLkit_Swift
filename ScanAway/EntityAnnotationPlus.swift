import Foundation
public final class EntityAnnotation {
     var zza = ""
    var zzb = 0
    var zzc = 0
    func StringGetAnnotatedText() {
        return EntityAnnotation.zza.substring(EntityAnnotation.zzb, EntityAnnotation.zzc)
     }
}
